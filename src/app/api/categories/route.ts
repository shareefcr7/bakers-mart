
import { NextResponse } from 'next/server';
import { getCategories, addCategory, Category } from '@/lib/db';

export async function GET() {
  try {
    const categories = getCategories();
    return NextResponse.json(categories);
  } catch (error) {
    return NextResponse.json({ error: 'Failed to fetch categories' }, { status: 500 });
  }
}

export async function POST(request: Request) {
  try {
    const body = await request.json();
    
    if (!body.name) {
        return NextResponse.json({ error: 'Name is required' }, { status: 400 });
    }

    const newCategory: Category = {
      name: body.name,
      image: body.image || '/images/placeholder.png',
      // id will be generated by addCategory if not provided, but usually we don't provide it here
    };

    try {
        const added = addCategory(newCategory);
        return NextResponse.json(added, { status: 201 });
    } catch (e: any) {
        return NextResponse.json({ error: e.message }, { status: 400 });
    }
  } catch (error) {
    return NextResponse.json({ error: 'Failed to create category' }, { status: 500 });
  }
}
